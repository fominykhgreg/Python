a = float(int(input("Результат в первый день:")))
b = int(input("Результат должен быть:"))
i = 1
c = a
while c < b :
    print(i,"- й день:",round(c,2))
    c = c*1.1
    i += 1

print(i,"- й день:",round(c,2))
print(f'{"Ответ: на "}{i}{"-й день спортсмен достиг результата - не менее "}{int(c)}{" km."}')


